#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			length;
	char			*str;

	i = 0;
	length = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * length + 1);
	if (s == NULL)
		return (NULL);
	if (str == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
